// java program by string and int passing method..


public class String_int_passing {

    public static void my_method(String fname , int x){

        System.out.println(fname +" is "+ x);

    }
    public static void main(String[] args) {
        my_method("vishal",23);

        my_method("paritosh",23);

        my_method("kunal",22);
    }
}
